<section>
    <h2>2. Koje podatke prikupljamo</h2>
    <p>Od posjetilaca možemo obrađivati tehničke podatke potrebne za sigurnost i rad stranice, poput IP adrese, vrste uređaja, preglednika, vremena pristupa i posjećenih stranica. Google Analytics koristimo samo nakon vaše saglasnosti.</p>
    <p>Od registrovanih profesionalaca obrađujemo podatke unesene prilikom registracije i uređivanja profila: ime, e-mail adresu, lozinku u zaštićenom obliku, naziv firme, telefon, lokacije rada, opis, portfolio, dostupnost, društvene mreže i druge dobrovoljno objavljene informacije.</p>
</section>

<section>
    <h2>3. Zašto i po kom osnovu obrađujemo podatke</h2>
    <ul>
        <li>pružanje naloga, javnog profila, pretrage, portfolija i direktnog kontakta;</li>
        <li>sigurnost, sprečavanje zloupotrebe i pouzdan rad platforme;</li>
        <li>odgovaranje na upite i slanje obaveznih servisnih poruka;</li>
        <li>analitika korištenja i unapređenje usluge, isključivo na osnovu saglasnosti;</li>
        <li>ispunjavanje zakonskih obaveza i zaštita legitimnih prava.</li>
    </ul>
    <p>Pravni osnov može biti izvršenje ugovora, legitimni interes, zakonska obaveza ili vaša saglasnost, zavisno od konkretne obrade.</p>
</section>

<section>
    <h2>4. Javni profil i direktni kontakt</h2>
    <p>Podaci koje profesionalac objavi na profilu, uključujući fotografije, opis, lokacije, kontakt podatke i poveznice, dostupni su javnosti i mogu ih indeksirati internet pretraživači. Nemojte objavljivati podatke ili sadržaj za koji nemate pravo objave.</p>
</section>

<section>
    <h2>5. Kolačići i Google Analytics</h2>
    <p>Nužni kolačići koriste se za sesiju, sigurnost, prijavu i odabrani jezik. Neobavezna analitika aktivira se tek nakon izbora „Prihvati analitiku“. Saglasnost možete odbiti ili povući u bilo kojem trenutku putem opcije „Postavke kolačića“ u podnožju stranice.</p>
    <p>Kada je analitika prihvaćena, Google može obrađivati podatke o korištenju stranice u skladu sa svojim pravilima privatnosti. FotoMajstor ne koristi analitičke podatke za donošenje automatizovanih odluka o korisnicima.</p>
</section>

<section>
    <h2>6. Primaoci i prenosi podataka</h2>
    <p>Podatke možemo povjeriti pouzdanim pružaocima hostinga, e-mail dostave, sigurnosti i analitike samo u obimu potrebnom za pružanje usluge. Ako obrada uključuje prenos izvan vaše države ili Evropskog ekonomskog prostora, primjenjuju se odgovarajuće ugovorne i zakonske zaštitne mjere.</p>
</section>

<section>
    <h2>7. Čuvanje i zaštita podataka</h2>
    <p>Podatke čuvamo dok je nalog aktivan i onoliko dugo koliko je potrebno za navedene svrhe, rješavanje sporova i zakonske obaveze. Primjenjujemo razumne tehničke i organizacione mjere, ali nijedan online sistem ne može garantovati apsolutnu sigurnost.</p>
</section>

<section>
    <h2>8. Vaša prava</h2>
